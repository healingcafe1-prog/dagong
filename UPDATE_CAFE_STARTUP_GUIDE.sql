### 다공(茶工)이란?
**차(茶)와 공예(工)를 잇는 직거래 플랫폼**
- 전국 차 생산자와 공예 작가 네트워크
- 생산자 ↔ 소비자 직거래 시스템
- 오프라인 지역별 모임 커뮤니티
- 교육 프로그램 및 체험 공간

### 다공 회원 창업의 핵심 가치

**1. 합리적인 원가 구조**
- 생산자 직거래로 중간 유통 마진 절감
- 다공 플랫폼 회원 특별 가격
- 소량 주문 가능 (재고 부담 최소화)
- 계절별 신상품 우선 공급

**2. 차별화된 상품 구성**
- 일반 유통망에 없는 독특한 차와 공예품
- 작가 스토리가 있는 핸드메이드 제품
- 한정판 시즌 상품 독점 판매
- 지역 특산 차와 공예품 큐레이션

**3. 공동 마케팅 효과**
- 다공 플랫폼 회원 네트워크 공유
- 온라인 마케팅 지원 (SNS, 블로그)
- 지역별 모임 행사 협력
- 교육 프로그램 연계 고객 유입

**4. 지속적인 교육 지원**
- 다도교육 및 명상교육 수료증
- 차 소믈리에 및 티마스터 과정
- 공예 작가 워크숍 참여
- 창업주 네트워킹 모임

## 평수별 창업 모델

### 🏪 초소형 테이크아웃 모델 (5평 / 16.5㎡)

**컨셉**: 간편 테이크아웃 + 소량 판매
**투자 비용**: 약 3,000만원 ~ 5,000만원

**공간 구성**
- 카운터 및 작업 공간: 2평
- 상품 전시 선반: 1.5평
- 창고 및 준비실: 1.5평

**추천 입지**
- 대학가 상권
- 오피스 밀집 지역
- 지하철역 인근
- 병원/관공서 1층

**메뉴 구성**
- **차 음료** (10종)
  - 한국 녹차 라떼
  - 우롱차 라떼
  - 허브차 믹스
  - 시즌 한정 차 음료

- **판매 상품** (소량 큐레이션)
  - 티백 차 (10종)
  - 소형 찻잔 세트 (5종)
  - 차 관련 소품 (향초, 티코지 등)

**월 예상 매출**: 800만원 ~ 1,200만원
**손익분기점**: 5~7개월

**다공 플랫폼 활용**
- 주 2회 소량 발주로 신선도 유지
- 계절별 시즌 차 프로모션
- 다공 회원 할인 쿠폰 제공

