## 지역별 오프라인 모임 활성화 전략

### 다공 지역 챕터 운영

**목적**: 온라인 플랫폼 → 오프라인 커뮤니티 연결

**지역 챕터 구성**
- 서울 5개 권역 (강북/강남/강동/강서/강중)
- 경기 8개 권역 (수원/성남/고양/안양/부천/의정부/평택/화성)
- 지방 광역시별 (부산/대구/인천/광주/대전/울산)
- 도 단위 주요 도시

**정기 모임 활동**
1. **월례 다회** (매월 마지막 주 토요일)
   - 회원 카페에서 순회 개최
   - 신규 차 및 공예품 소개
   - 생산자 초청 토크
   - 네트워킹 타임

2. **차 문화 탐방** (분기별)
   - 차 산지 방문 (보성, 하동, 제주)
   - 공예 마을 투어 (이천, 여주, 담양)
   - 전통 시장 탐방
   - 문화재 견학

3. **창업주 모임** (격월)
   - 운영 노하우 공유
   - 공동 구매 협의
   - 마케팅 아이디어 교환
   - 멘토링 프로그램

4. **교육 프로그램** (수시)
   - 다도 마스터 클래스
   - 공예 워크숍
   - 창업 세미나
   - SNS 마케팅 교육

### 온·오프 연계 이벤트

**봄 (3~5월)**
- 새싹 녹차 축제
- 도자기 만들기 체험
- 벚꽃 다회

**여름 (6~8월)**
- 냉차 페스티벌
- 여름 공예 전시회
- 힐링 차 명상 캠프

**가을 (9~11월)**
- 가을 차 수확 체험
- 전통 공예 박람회
- 추석 선물 세트 페어

**겨울 (12~2월)**
- 겨울 따뜻한 차 페스티벌
- 송년 다회
- 신년 복주머니 만들기

### 지역 상생 프로그램

**생산자 ↔ 창업주 직거래**
- 정기 방문 및 소량 구매
- 시즌별 신상품 우선 공급
- 공동 상품 기획 (PB 제품)

