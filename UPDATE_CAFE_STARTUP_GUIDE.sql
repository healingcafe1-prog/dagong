### 🏛️ 중형 전시 카페 모델 (18평 / 59.4㎡)

**컨셉**: 카페 + 갤러리 + 체험 공간
**투자 비용**: 약 1억원 ~ 1억 5,000만원

**공간 구성**
- 카운터 및 바: 3평
- 테이블 좌석 (8~12석): 8평
- 갤러리 전시 공간: 4평
- 체험 공간 (다도실): 2평
- 창고 및 화장실: 1평

**추천 입지**
- 번화가 2층
- 문화센터 인근
- 예술촌/공방 거리
- 관광지 상권

**메뉴 구성**
- **차 음료** (20종 이상)
- **시그니처 음료** (5종)
- **디저트** (10종)
- **런치 세트** (차 + 간식)

- **판매 상품** (대규모 큐레이션)
  - 리프티 100종 이상
  - 다기 세트 50종
  - 공예품 100종 (도자기, 목공예, 섬유)
  - 차 관련 서적 및 잡화

**특별 프로그램**
- 주말 다도 체험 클래스 (1회 4명, 5만원)
- 월 1회 차 문화 강좌
- 분기별 작가 전시회
- 다공 회원 모임 공간 제공

**월 예상 매출**: 3,000만원 ~ 5,000만원
**손익분기점**: 12~18개월

**다공 플랫폼 활용**
- 매달 "이달의 작가" 선정 전시
- 다공 생산자 초청 이벤트
- 온라인 주문 → 매장 픽업 서비스

