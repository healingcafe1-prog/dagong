### ☕ 소형 카페 모델 (9평 / 29.7㎡)

**컨셉**: 카페 + 전시 판매 + 소모임 공간
**투자 비용**: 약 5,000만원 ~ 8,000만원

**공간 구성**
- 카운터 및 바: 2평
- 테이블 좌석 (4~6석): 4평
- 상품 전시 공간: 2평
- 창고 및 화장실: 1평

**추천 입지**
- 주택가 상권
- 문화 거리
- 소규모 상가 1층
- 조용한 골목

**메뉴 구성**
- **차 음료** (15종)
- **간단한 디저트** (5종)
  - 차 쿠키
  - 떡케이크
  - 전통 다과

- **판매 상품** (중규모 큐레이션)
  - 리프티 50종
  - 다기 세트 20종
  - 소형 공예품 30종

**월 예상 매출**: 1,500만원 ~ 2,500만원
**손익분기점**: 8~12개월

**다공 플랫폼 활용**
- 주 1회 "작가와의 만남" 행사
- 월 1회 소모임 공간 무료 제공
- 다공 회원 대상 차 시음회

