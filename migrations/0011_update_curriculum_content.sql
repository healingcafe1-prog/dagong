3. 시유 기법
① 담금법 (Dipping)
  - 초벌 그릇을 유약에 담그기
  - 3-5초 담궜다가 꺼내기
  - 장점: 균일하고 빠름

② 붓칠법 (Brushing)
  - 붓으로 유약 바르기
  - 2-3번 덧칠
  - 용도: 부분 유약, 장식

③ 분무법 (Spraying)
  - 스프레이 건으로 뿌리기
  - 용도: 그라데이션, 큰 작품

④ 흘림법 (Pouring)
  - 유약을 부어 흘려내리기
  - 용도: 안쪽, 독특한 효과

